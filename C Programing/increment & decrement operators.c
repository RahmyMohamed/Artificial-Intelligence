#include<stdio.h>
int main ()

{
    int x =10;
            // Increment

    printf ("x = %d\n",x);
    printf ("++x = %d\n",++x); // pre increement
    printf ("x = %d\n",x);
    printf ("x++ = %d\n",x++); // post increment
    printf ("x = %d\n",x);

            // Decrement

    printf ("--x = %d\n",--x); // pre decrement
    printf ("x = %d\n",x);
    printf ("x-- = %d\n",x--); // post decrement
    printf ("x = %d\n",x);
}
